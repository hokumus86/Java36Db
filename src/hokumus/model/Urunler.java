package hokumus.model;

public class Urunler {
	
	private String urunAdi;
	private int fiyat;
	private String mesei;
	
	public String getUrunAdi() {
		return urunAdi;
	}
	public void setUrunAdi(String urunAdi) {
		this.urunAdi = urunAdi;
	}
	public int getFiyat() {
		return fiyat;
	}
	public void setFiyat(int fiyat) {
		this.fiyat = fiyat;
	}
	public String getMesei() {
		return mesei;
	}
	public void setMesei(String mesei) {
		this.mesei = mesei;
	}

}
